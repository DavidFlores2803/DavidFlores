    $userInput = Read-Host("There are $numOfProcesses with the name $processName, proceed? Y/N")

    if($userInput.ToLower() -eq "y")
    {
        Write-Host -ForegroundColor Magenta -BackgroundColor Yellow "Procedure Worked !"
        Get-Process $processName | Stop-Process
    }
    else
    {
        Write-Host -ForegroundColor Magenta -BackgroundColor Yellow "Procedure did not work! No processes were stopped"
    }

}
